<?php

//Operadores aritmeticos

$numero1 = 55;
$numero2 = 33;

echo 'Suma: ' . ($numero1 + $numero2) . '</br>';
echo 'Resta: ' . ($numero1 - $numero2) . '</br>';
echo 'Multiplicacion: ' . ($numero1 * $numero2) . '</br>';
echo 'Division: ' . ($numero1 / $numero2) . '</br>';
echo 'Resto: ' . ($numero1 % $numero2) . '</br>';

